--Find employees whose salary is equal to department maximum salary using JOIN
SELECT E.* FROM EMPLOYEES E
INNER JOIN (SELECT DEPT_ID, MAX(SALARY) AS MAX_SAL 
FROM EMPLOYEES
GROUP BY DEPT_ID) D
ON E.DEPT_ID = D.DEPT_ID
WHERE E.SALARY = D.MAX_SAL;

